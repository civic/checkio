MOVE = {"S": (1, 0), "N": (-1, 0), "W": (0, -1), "E": (0, 1)}

class Node:
    def __init__(self, pos, val):
        self.pos = pos
        self.val = val
        """:type: tuple """
        self.status = None
        """:type: str """
        self.r_cost = 0
        self.h_cost = 0
        self.score = 0
        self.parent = None
        self.route = ""
        """:type: Node """

    def open(self, goal, parent, move):
        pos = self.pos
        self.parent = parent
        self.r_cost = self.parent.r_cost + 1 if self.parent is not None else 0
        self.h_cost = (goal[0] - pos[0]) + (goal[1] - pos[1])
        self.score = self.r_cost + self.h_cost
        self.status = "O"

        if move is not None:
            self.route = parent.route + move

    def __repr__(self):
        return "({pos[0]},{pos[1]}){v}:{status},{r_cost},{h_cost},{score}".format(
            pos=self.pos,
            v = self.val,
            status=self.status if self.status is not None else "-",
            r_cost=self.r_cost,
            h_cost=self.h_cost,
            score=self.score
        )
def search(start, goal, maze_map):

    nodes = [[Node((y, x), v) for x, v in enumerate(row)] for y, row in enumerate(maze_map)]
    start_node = nodes[start[0]][start[1]]
    start_node.open(goal, None, None)
    open_list = []
    open_list.append(start_node)

    while True:
        min_score_node = min(open_list, key=lambda n: n.score)
        for node in (n for n in open_list if n.score == min_score_node.score):
            pos = node.pos

            for move in MOVE.items():
                move_direction = move[0]
                m = move[1]
                next_node = nodes[pos[0]+m[0]][pos[1]+m[1]]
                if next_node.val != 0:
                    continue
                if next_node.status is not None:
                    continue

                next_node.open(goal, node, move_direction)
                if next_node.pos == goal:
                    return next_node

                open_list.append(next_node)

            open_list.remove(node)
            node.status = "C"


def checkio(maze_map):
    start = (1, 1)
    goal = (10, 10)

    found = search(start, goal, maze_map)
    print("{0}".format(found.route))

    return found.route


