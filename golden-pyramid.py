import itertools

def how_many_gold(pyramid, route):
    idx = 0
    gold = pyramid[0][0]
    for i, r in enumerate(route):
        idx += r
        gold += pyramid[i+1][idx]

    return gold

def count_gold(pyramid):
    level = len(pyramid)

    max_ptn = max(itertools.product(range(2), repeat=level - 1), key=lambda ptn: how_many_gold(pyramid, ptn))

    return how_many_gold(pyramid, max_ptn)


