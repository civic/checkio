def count_neighbours(grid, row, col):
    count = 0
    for r in range(row-1, row+2):
        for c in range(col-1, col+2):
            if (r < 0) or (c < 0) or (r >= len(grid)) or  (c >= len(grid[0])) or (r==row and c==col):
                continue
            count += grid[r][c]
    return count


